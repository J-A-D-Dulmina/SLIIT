public class ex6 {
    public static void main(String[] args) {

        int i= 24 ,j= 41;
        double x = 12.54, y = 8.245;

        System.out.println("\nVariable values" + "\nj ="+ i + "\nj =" + j + "\nx =" + x + "\ny =" +y);

        //addong numbers
        System.out.println("\nAdding" + "\ni + j ="+(i+j) + "\nx + y ="+(x+y));

        //substracting numbers
        System.out.println("\nsubstracting" + "\ni - j ="+(i-j) + "\nx - y ="+(x-y) );

        //multipling numbers
        System.out.println("\nmultipling" + "\ni * j ="+(i*j) + "\nx * y ="+(x*y) );

        //dividing numbers
        System.out.println("\ndividing" + "\ni / j ="+(i/j) + "\nx / y ="+(x/y) );

        //the remainder resulting from dividing numbers
        System.out.println("\ndividing" + "\ni % j ="+(i%j) + "\nx % y ="+(x%y) );

        //mixing types
        System.out.println("\nmixing types" + "\ni + x ="+(i+x) + "\ni * x ="+(i*x) );

        
    }
    
}
